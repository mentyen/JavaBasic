package com.class10;

public class Task4 {
	public static void main(String[] args) {
		
		for(int i=1;i<=5;i++) {
			for(int b=1;b<=i;b++) {
				System.out.print("*");
			}
			System.out.println();
		}
		for(int c=4;c>0;c--) {
			for(int d=1;d<=c;d++) {
				System.out.print("*");
			}
			System.out.println();
		}
	}

}
