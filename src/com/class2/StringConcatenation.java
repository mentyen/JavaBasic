package com.class2;

public class StringConcatenation {
public static void main(String[] args) {
	
	String name = "John";
	String lastName = "Smith";
	char grade = 'B';
	String city = "Brooklyn";
	String state = "NY";
	long phoneNumber = 456789876;
	
	System.out.println("My name is "+ name + " and my last name is "+lastName);
	System.out.println("I am " + grade+ " student");
	System.out.println("I live in "+ city+ " in state of "+ state);
	System.out.println("My phone number is " + phoneNumber);
	System.out.println();
	
	System.out.println("My name is "+ name + " and my last name is "+lastName+ "\n"+"I am " + grade+ " student"+ "\n"+ "I live in "+ city+ " in state of "+ state+"\n"+"My phone number is " + phoneNumber);
	
	
	System.out.println();
	
	String day = "Sunday";
	System.out.println("Today is " + day);
	
	
	
	
}
}
