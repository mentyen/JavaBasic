package com.class26_THIS;

public class Overloading_VS_Overiding {
	/*
	 * OVRLOADING- method name will be the SAME in a same class
	 * OVERRIDING-method NAME is SAME in PArent and CHILD class
	 * 
	 * DIFFERENCES
	 * OVERLOADING must have different parameters
	 * OVERRIDING must be THE SAME parameters
	 * 
	 * OVERLOADING signature must be DIFFERENT
	 * OVERRIDING signature must be SAME
	 * 
	 * OVERLOADING RETURN type can be DIFFERENT
	 * OVERRIDING RETURN type must be the SAME
	 * 
	 * OVERLOADING we do not need INFERITANCE
	 * OVERRIDING INFERITANCE is MUST
	 * 
	 * OVERLOAD STATIC is okay
	 * OVERRIDE STATIC is not
	 * 
	 * OVERLOAD CONSTRUCTOR Is okay
	 * OVERRIDE CONSTRUCTOR is not
	 * 
	 * OVERLOAD PRIVATE is okay
	 * OVERRIDE PRIVATE is not
	 * 
	 * OVERLOAD no restrictions for access modifiers
	 * OVERRIDE can not be more restricted then parent
	 * 
	 * OVERLOAD Final is okay
	 * OVERRIDE FINAL is not okay
	 * 
	 */

}
